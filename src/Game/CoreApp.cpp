//=============================================================================
void Print(const std::string& msg)
{
	puts(msg.c_str());
}
//=============================================================================
void Warning(const std::string& msg)
{
	Print("Warning: " + msg);
}
//=============================================================================
void Error(const std::string& msg)
{
	Print("Error: " + msg);
}
//=============================================================================
void Fatal(const std::string& msg)
{
	Print("Fatal: " + msg);
	app::Exit();
}
//=============================================================================
void app::Exit()
{
	isExit = true;
}
